        public MainWindow()
        {
            InitializeComponent();
        }

        private void Button_Click(object sender, RoutedEventArgs e)
        {
            if (UserViewModel.ConnectUser(Login.Text, Pass.Text))
            {
                Welcome welcome = new Welcome();
                welcome.Show();
                this.Close();
            }
            else
            {
                MessageBox.Show("Login ou mot de passe incorrect");
            }
        }
